package dev.vernonlim.cw2024game.managers;

/**
 * An enum representing the different Actions a user can take in-game.
 */
public enum Action {
    /**
     * The Action representing moving Up
     */
    UP,

    /**
     * The Action representing moving Down
     */
    DOWN,

    /**
     * The Action representing moving Left
     */
    LEFT,

    /**
     * The Action representing moving Right
     */
    RIGHT,

    /**
     * The Action representing Firing a weapon
     */
    FIRE,

    /**
     * The Action representing entering Focus Fire mode
     */
    FOCUS,

    /**
     * The Action representing toggling Pausing
     */
    PAUSE,

    /**
     * The Action representing triggering a button in menus
     */
    CONFIRM,
}
