package dev.vernonlim.cw2024game.screens;

/**
 * An enum representing the Screens available in the game
 */
public enum ScreenCode {
    /**
     * The Main Menu allowing users to start the game.
     */
    MAIN_MENU,

    /**
     * The Character Select menu allowing users to select a character.
     */
    CHARACTER_SELECT,

    /**
     * The Level Select menu allowing users to select a Level after selecting a character.
     */
    LEVEL_SELECT,

    /**
     * The first gameplay Level.
     */
    LEVEL_ONE,

    /**
     * The second gameplay Level.
     */
    LEVEL_TWO,

    /**
     * The third gameplay Level.
     */
    LEVEL_THREE,

    /**
     * The fourth gameplay Level.
     */
    LEVEL_FOUR,
}
