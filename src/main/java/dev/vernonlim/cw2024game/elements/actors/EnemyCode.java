package dev.vernonlim.cw2024game.elements.actors;

/**
 * An enum representing the types of Enemy Plane present in the game.
 */
public enum EnemyCode {
    /**
     * An ordinary enemy plane.
     */
    ENEMY_PLANE,

    /**
     * A blue plane that shoots bullets diagonally.
     */
    ENEMY_BLUE,

    /**
     * A red plane that moves fast.
     */
    ENEMY_RED,

    /**
     * A boss plane.
     */
    BOSS,
}
